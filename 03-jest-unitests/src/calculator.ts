export class Calculadora{
    constructor(){}

    sumNumber(numb1: number, numb2: number,){
        return numb1 + numb2;
    }
    sumNumber1(numb1: number, numb2: number,){
        return numb1 + numb2;
    }
    restNumber(numb1: number, numb2: number,){
        return numb1 - numb2;
    }
    restNumber1(numb1: number, numb2: number,){
        return numb1 - numb2;
    }
    
}